package border.cake.maratonajava.introducao;

public class Aula05EstruturasCondicionais03 {
    public static void main(String[] args) {
        // Doar se salário > 5000
        double salary = 6000;
        // (condiçao) ? verdadeiro : falso
        String result = salary > 5000 ? "Eu voar doar 500 pro DevDojo" : "Ainda não tenho condições, mas vou ter!";
        System.out.println(result);
    }
}
